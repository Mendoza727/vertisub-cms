            </div>
            <div class="section-content-privacy">
                <p>Para información, claridad, comprensión y desarrollo de la presente Política de Protección de Datos Personales de VERTISUB COLOMBIA S.A.S. (www.vertisub.com) es necesario establecer la definición de los siguientes términos:</p>

                <p><strong>a. Titular de la información:</strong> Es la persona natural o jurídica a quien se refiere la información que reposa ya sea en un banco de datos, base de datos, archivos físicos y/o electrónicos.</p>

                <p><strong>b. Fuente de la Información:</strong> Es la persona, entidad u organización que recibe o conoce datos personales de los titulares de la información en virtud de una relación comercial de servicio o de cualquier otra índole y que en razón de autorización legal o del titular suministra esos datos a un operador el que a su vez los entrega a un usuario final. La fuente de la información responde por la calidad de los datos suministrados al operador.</p>

                <p><strong>c. Operador de la información:</strong> Es la persona entidad u organización que recibe de la fuente datos personales sobre varios titulares de la información, los administra y los pone en conocimiento de los usuarios bajo parámetros legales, cumpliendo los deberes y responsabilidades previstas para garantizar la protección de los derechos del titular de los datos.</p>

                <p><strong>d. Usuario:</strong> Es la persona natural o jurídica que conforme a la ley puede acceder a la información personal de uno o varios titulares de la información, sujeto a los deberes y responsabilidades previstos para garantizar los derechos del titular de los datos.</p>

                <p><strong>e. Tercero:</strong> Persona externa a la compañía que sin tener una relación laboral, contractual o comercial suministra o solicita se le circulen datos de carácter personal derivados de un vínculo legal o contractual con el titular u operador de la información, o por orden judicial de autoridad competente, sin que ello lo exima de las obligaciones que para el tratamiento de Datos Personales exige la ley.</p>

                <p><strong>f. Autorización:</strong> Es el consentimiento previo, informado y expreso dado por la persona titular y/o legitimado para llevar a cabo el tratamiento de datos personales.</p>

                <p><strong>g. Base de datos:</strong> Conjunto organizado de datos personales que sea objeto del tratamiento, cobija los archivos entendidos como depósitos ordenandos de datos.</p>

                <p><strong>h. Archivo:</strong> Conjunto organizado o deposito ordenado de datos personales sujetos a tratamiento.</p>

                <p><strong>i. Dato Personal:</strong> Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables.</p>

                <p><strong>j. Dato Sensible:</strong> Son aquellos que afectan la intimidad del titular o cuyo uso indebido puede generar su discriminación, tales como aquellos que revelan el origen racial o étnico, la orientación política, las convicciones religiosas o filosóficas, la pertenencia a sindicatos, promoción de intereses de cualquier partido políticos o que garanticen los derechos y garantías de partidos políticos de oposición, organizaciones sociales de derechos humanos, datos relativos a la salud, la vida sexual, y datos biométricos.</p>

                <p><strong>k. Dato Público:</strong> Es el dato calificado como tal según mandato legal o constitucional y todos aquellos que no sean privados o semiprivados, son públicos, entre otros, los datos contenidos en documentos públicos, sentencias judiciales debidamente ejecutoriadas, que no estén sometido a reserva y los relativos al estado civil de las personas.</p>

                <p><strong>l. Dato Privado:</strong> Es el dato que por su naturaleza intima o reservada solo es relevante para el titular.</p>

                <p><strong>m. Dato Semiprivado:</strong> Es semiprivado el dato que no tiene naturaleza intima, reservada, ni publica y cuyo conocimiento o divulgación puede interesar no solo a su titular sino a cierto sector o grupo de personas o a la sociedad en general como el dato financiero, crediticio, de actividad comercial o de servicios.</p>

                <p><strong>n. Encargado del tratamiento:</strong> Persona natural o jurídica, pública o privada que por sí misma o en asocio con otros, realice el tratamiento de datos personales por cuenta del responsable del tratamiento.</p>

                <p><strong>o. Responsable del tratamiento:</strong> Persona natural o jurídica pública o privada que por sí misma o en asocio con otros decida sobre la base de datos y/o el tratamiento de los datos.</p>

                <p><strong>p. Tratamiento:</strong> Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección, almacenamiento, uso, circulación o supresión.</p>

                <p><strong>q. Tratamiento según los estándares Europeos:</strong> Cualquier operación o conjunto de operaciones sean o no automatizados que se aplican a datos de carácter personal en especial su recogida, conservación, utilización, revelación o supresión.</p>

                <p><strong>r. Transferencia:</strong> La transferencia de datos tiene lugar cuando el responsable y/o encargado del tratamiento de datos personales, ubicado en Colombia, envía la información o los datos personales a un receptor, que a su vez es responsable del tratamiento y se encuentra dentro o fuera del país.</p>

                <p><strong>s. Transmisión:</strong> Tratamiento de datos personales que implica la comunicación de los mismos dentro o fuera del territorio de la República de Colombia cuando tenga por objeto la realización de un tratamiento por el encargado por cuenta del responsable.</p>

                <p><strong>t. Hipervínculos a portales de terceros:</strong> Los portales de VERTISUB COLOMBIA S.A.S. (www.vertisub.com) contiene hipervínculos que re-direccionan a portales web o de internet de terceros, cualquier acceso y/o uso que se haga de las páginas web de terceros a las que se re direcciona al usuario, no se rigen por la presente Política de tratamiento y Procedimientos de datos Personales. VERTISUB COLOMBIA S.A.S. (www.vertisub.com) no se hace responsable de la información y practicas llevadas a cabo por terceros en sus propios portales web.</p>
