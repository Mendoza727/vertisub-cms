            <div class="section-content-privacy">
                <p>Todos los sujetos en el desarrollo, interpretación y aplicación de la presente Política de Datos y en especial VERTISUB COLOMBIA S.A.S. (www.vertisub.com) deberá aplicar de manera preferente, armónica e integral los siguientes principios:</p>

                <p><strong>a. Principio de legalidad en el tratamiento de datos:</strong> El tratamiento de datos es una actividad reglada que debe sujetarse a lo establecido en ella y demás disposiciones que lo regulen.</p>

                <p><strong>b. Principio de finalidad:</strong> El tratamiento debe obedecer a una finalidad legítima de acuerdo a la Constitución y la ley.</p>

                <p><strong>c. Principio de libertad:</strong> El tratamiento solo puede ejercerse con el consentimiento previo, expreso e informado del titular. Los datos personales no podrán ser obtenidos o divulgados sin previa autorización, o en ausencia de mandato legal o judicial que revele el consentimiento.</p>

                <p><strong>d. Principio de Veracidad o Calidad:</strong> La información sujeta a tratamiento debe ser veraz, completa, exacta, actualizada, comprobable y comprensible. Se prohíbe el tratamiento de datos parciales, incompletos, fraccionados o que induzcan al error.</p>

                <p><strong>e. Principio de transparencia:</strong> En el tratamiento debe garantizarse el derecho del titular a obtener del responsable del tratamiento o del encargado del tratamiento, en cualquier momento y sin restricciones, información acerca de la existencia de datos que le concierna.</p>

                <p><strong>f. Principio de acceso y circulación restringida:</strong> El tratamiento se sujeta a los límites que se deriven de la naturaleza de los datos personales, de la ley 1581 de 2012, Decreto 1377 de 2013 y la Constitución Nacional, en ese sentido el tratamiento solo podrá hacerse por personas autorizadas por el titular. Los datos personales, salvo la información pública, no podrán estar disponible en internet u otros medios tecnológicos de comunicación o divulgación masiva.</p>

                <p><strong>g. Principio de seguridad:</strong> La información sujeta a tratamiento por el Responsable del tratamiento o encargado del tratamiento se deberá manejar con las medidas técnicas, humanas y administrativas que sean necesarias para otorgar la seguridad a los registros evitando, su adulteración, perdida, consulta, uso o acceso no autorizado o fraudulento.</p>

                <p><strong>h. Principio de confidencialidad:</strong> Todas las personas que intervengan en el tratamiento de datos personales que no tengan la naturaleza de públicos están obligadas a garantizar la reserva de la información inclusive después de finalizada su relación con alguna de las labores que comprenden el tratamiento.</p>
