        function downloadPolicy() {
            const content = generatePolicyText();
            const blob = new Blob([content], {
                type: 'text/plain;charset=utf-8'
            });
            const url = URL.createObjectURL(blob);

            const link = document.createElement('a');
            link.href = url;
            link.download = `politica-datos-personales-vertisub-${new Date().toISOString().split('T')[0]}.txt`;
            document.body.appendChild(link);
            link.click();
            document.body.removeChild(link);
            URL.revokeObjectURL(url);

            showNotification('Política descargada correctamente', 'success');
        }

        function generatePolicyText() {
            let content = 'POLÍTICA DE TRATAMIENTO DE DATOS PERSONALES\n';
            content += 'VERTISUB COLOMBIA S.A.S.\n';
            content += '='.repeat(60) + '\n\n';

            const sections = document.querySelectorAll('.privacy-section-privacy');
            sections.forEach(section => {
                const title = section.querySelector('.section-title-privacy').textContent;
                const text = section.querySelector('.section-content-privacy').innerText;

                content += title + '\n';
                content += '-'.repeat(title.length) + '\n';
                content += text.replace(/\s+/g, ' ').trim() + '\n\n';
            });

            content += '\n' + '='.repeat(60) + '\n';
            content += 'Documento generado el: ' + new Date().toLocaleString() + '\n';
            content += 'NIT: 900.570.681-5\n';
            content += 'Conforme a la Ley 1581 de 2012 y Decreto 1377 de 2013\n';

            return content;
        }

